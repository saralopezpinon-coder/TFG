n_consultas = len(consultas_unicas)

marcas_iter = defaultdict(set)
for row in filas:
    for m in row.get('MARCAS_CITADAS','').split(' | '):
        m = m.strip()
        if m: marcas_iter[(row['ID_CONSULTA'], row['ITERACION'])].add(m)

todas_marcas = set(m for ms in marcas_iter.values() for m in ms)
stats = {}
for marca in todas_marcas:
    consultas_con = sum(1 for cid in consultas_unicas
                       if any(marca in marcas_iter.get((cid,str(it)),set()) for it in range(1,6)))
    stats[marca] = consultas_con

niveles = defaultdict(lambda: Counter())
for row in filas:
    for par in row.get('NIVELES_MENCION','').split(' | '):
        if ':' in par:
            marca, nivel = par.split(':',1)
            marca, nivel = marca.strip(), nivel.strip().lower()
            if marca:
                if 'principal' in nivel or 'parrafo' in nivel:
                    niveles[marca]['principal'] += 1
                elif 'bullet' in nivel or 'secundar' in nivel:
                    niveles[marca]['bullet'] += 1
                else:
                    niveles[marca]['namedrop'] += 1

def top10_nivel(nivel_key, min_consultas=10):
    scores = []
    for marca in todas_marcas:
        if stats.get(marca,0) < min_consultas: continue
        niv = niveles[marca]
        total_niv = sum(niv.values()) or 1
        pct = round(niv[nivel_key]/total_niv*100,1)
        scores.append((marca, pct, stats[marca]))
    return sorted(scores, key=lambda x:x[1], reverse=True)[:10]

