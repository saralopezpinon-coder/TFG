marcas_iter = defaultdict(set)
for row in filas:
    for m in row.get('MARCAS_CITADAS', '').split(' | '):
        m = m.strip()
        if m: marcas_iter[(row['ID_CONSULTA'], row['ITERACION'])].add(m)

