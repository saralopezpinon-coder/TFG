stats = {}
for marca in todas_marcas:
    tasas = [sum(1 for it in range(1,6)
             if marca in marcas_iter.get((cid,str(it)),set()))/5
             for cid in consultas_unicas]
    p = sum(tasas)/n_consultas
    var = sum((x-p)**2 for x in tasas)/(n_consultas-1) if n_consultas>1 else 0
    std = math.sqrt(var)
    me = 1.96*std/math.sqrt(n_consultas)
    consultas_con = sum(1 for x in tasas if x>0)
    stats[marca] = {
        'consultas': consultas_con,
        'pct': round(consultas_con/n_consultas*100,1),
        'std': round(std*100,2),
        'me': round(me*100,2),
    }

